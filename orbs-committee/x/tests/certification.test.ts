import {
  assert,
  describe,
  test,
  clearStore,
  beforeAll,
  afterAll
} from "matchstick-as/assembly/index"
import { Address } from "@graphprotocol/graph-ts"
import { ContractRegistryAddressUpdated } from "../generated/schema"
import { ContractRegistryAddressUpdated as ContractRegistryAddressUpdatedEvent } from "../generated/Certification/Certification"
import { handleContractRegistryAddressUpdated } from "../src/certification"
import { createContractRegistryAddressUpdatedEvent } from "./certification-utils"

// Tests structure (matchstick-as >=0.5.0)
// https://thegraph.com/docs/en/developer/matchstick/#tests-structure-0-5-0

describe("Describe entity assertions", () => {
  beforeAll(() => {
    let addr = Address.fromString("0x0000000000000000000000000000000000000001")
    let newContractRegistryAddressUpdatedEvent =
      createContractRegistryAddressUpdatedEvent(addr)
    handleContractRegistryAddressUpdated(newContractRegistryAddressUpdatedEvent)
  })

  afterAll(() => {
    clearStore()
  })

  // For more test scenarios, see:
  // https://thegraph.com/docs/en/developer/matchstick/#write-a-unit-test

  test("ContractRegistryAddressUpdated created and stored", () => {
    assert.entityCount("ContractRegistryAddressUpdated", 1)

    // 0xa16081f360e3847006db660bae1c6d1b2e17ec2a is the default address used in newMockEvent() function
    assert.fieldEquals(
      "ContractRegistryAddressUpdated",
      "0xa16081f360e3847006db660bae1c6d1b2e17ec2a-1",
      "addr",
      "0x0000000000000000000000000000000000000001"
    )

    // More assert options:
    // https://thegraph.com/docs/en/developer/matchstick/#asserts
  })
})
